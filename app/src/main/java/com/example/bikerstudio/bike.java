package com.example.bikerstudio;

public class bike {

    private String id;
    private String bike_name;
    private String bike_model;
    private  String bike_version;






    public void setId(String id) {
        this.id = id;
    }

    public void setBike_name(String bike_name) {
        this.bike_name = bike_name;
    }

    public void setBike_model(String bike_model) {
        this.bike_model = bike_model;
    }

    public void setBike_version(String bike_version) {
        this.bike_version = bike_version;
    }

    public String getId() {
        return id;
    }

    public String getBike_name() {
        return bike_name;
    }

    public String getBike_model() {
        return bike_model;
    }

    public String getBike_version() {
        return bike_version;
    }




}
